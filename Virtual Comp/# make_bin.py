# make_bin.py
with open("test.bin", "wb") as f:
    # PUSH 5
    f.write(bytes([0x01, 0x05, 0x00, 0x00]))
    # MOV R0 10
    f.write(bytes([0x02, 0x00, 0x0A, 0x00]))
    # LOADR R0
    f.write(bytes([0x05, 0x00, 0x00, 0x00]))
    # PRINT
    f.write(bytes([0x07, 0x00, 0x00, 0x00]))
    # CEASE
    f.write(bytes([0x14, 0x00, 0x00, 0x00]))
